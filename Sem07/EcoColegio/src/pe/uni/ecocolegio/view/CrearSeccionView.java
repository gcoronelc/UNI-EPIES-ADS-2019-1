    private void llenarComboPeriodos(){
        // Datos
        List<String> lista = control.getPeriodos();
        // Proceso
        cboPeriodo.removeAllItems();
        if( control.getEstado() == 1 ){
            for (String periodo : lista) {
                cboPeriodo.addItem(periodo);
            }
            cboPeriodo.setSelectedIndex(-1);
        }
    }
    
    
    
